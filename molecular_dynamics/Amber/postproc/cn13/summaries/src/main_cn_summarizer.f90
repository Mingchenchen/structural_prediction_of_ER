program cn_summarizer
implicit none
integer :: i,j,nres,ntraj=-3,ios,idummy
integer :: nitems	! function
real*8, dimension(:,:), allocatable :: cn_real
integer, dimension(:), allocatable :: res_num,cn
character(len=2), dimension(:), allocatable :: chain_id
character(len=3), dimension(:), allocatable :: res_name
CHARACTER(LEN=300) :: cn_in				! cn file generated by dynamic_cn.f90.
!CHARACTER(LEN=300) :: rsa_out			! RSA file containing the normalized cn values.
CHARACTER(LEN=300) :: cn_sum			! RSA summary file (containing the average, median, stdev).
character(len=15000) :: line
character(len=15) :: dummy_char
character(len=100) :: cn_output_format,res_output_format,resnum_output_format
real*8 :: MRSA_finder,select	! functions
real*8 :: max_rsa,mean_cn,med_cn,stdev_rsa,var_cn,var2mean

if (command_argument_count()/=2) then
	write(*,*)
	write(*,*) "Incorrect number of input arguments on the command line."
	write(*,*) "Correct use:"
	write(*,*) "./a.out <input cn file> <output cn summary file>"
	write(*,*)
	stop
end if
call get_command_argument(1,cn_in)
call get_command_argument(2,cn_sum)
!call get_command_argument(3,rsa_sum)

! NOW DETERMINE THE NUMBER OF COLUMNS AND LINES IN THE INPUT cn FILE. 
	open(unit=11,file=trim(adjustl(cn_in)),status='old')
	read(11,'(1A15000)') line
	nres = nitems(line)-1
	write(*,*) '# of residues in cn file: ',nres
	do
		read(11,'(1A15000)',iostat=ios) line
		if (ios<0) then
			exit
		elseif (ios>0) then
			write(*,*) 'Sum Tin Wong! : PDB file broken.'; stop
		else
			ntraj = ntraj + 1
		end if
		cycle
	end do
	write(*,*) '# of MD trajectories in cn file: ',ntraj
	close(11)

open(unit=11,file=trim(adjustl(cn_in)),status='old')
open(unit=22,file=trim(adjustl(cn_sum)),status='replace')
	
allocate(cn_real(0:ntraj,nres),cn(nres),res_num(nres),res_name(nres),chain_id(nres))

! NOW PREPARE THE cn OUTPUT FILE.
	write(dummy_char,*) nres
	cn_output_format = trim(adjustl('(1I15,' // trim(adjustl(dummy_char)) // 'I15)'))
	resnum_output_format = trim(adjustl('(1A15,' // trim(adjustl(dummy_char)) // 'I15)'))
	res_output_format = trim(adjustl('(1A15,' // trim(adjustl(dummy_char)) // 'A15)'))

! NOW READ THE HEADER OF THE INPUT FILE
	read(11,res_output_format) dummy_char,res_name(1:nres)
	read(11,resnum_output_format) dummy_char,res_num(1:nres)
	read(11,res_output_format) dummy_char,chain_id(1:nres)

! NOW READ THE cn VALUES.
	do i = 0,ntraj
		read(11,cn_output_format) idummy,cn(1:nres)
		forall(j=1:nres) cn_real(i,j) = real(cn(j))
		if (idummy/=i) then; write(*,*) 'idummy /= i : ', idummy, i; stop; end if
	end do

! NOW WRITE OUT THE RSA SUMMARY FILE.
	write(22,'(8A15)') 'AMINO_ACID','RES_NUM','CHAIN','CRYSTAL_CN','MEAN_CN','MEDIAN_CN','VAR_CN','VAR2MEAN_CN'
	do i = 1,nres
		call avevar(cn_real(1:ntraj,i),ntraj,mean_cn,var_cn)
		var2mean = var_cn/mean_cn
		!stdev_rsa = sqrt(var_cn)
		!write(*,*) mean_rsa_dummy
		!write(*,*) mean_cn
		med_cn = select(ntraj/2,ntraj,cn_real(1:ntraj,i))
		write(22,'(1A15,1I15,1A15,5E15.5)') res_name(i),res_num(i),chain_id(i),cn_real(0,i),mean_cn,med_cn,var_cn,var2mean
	end do

end program cn_summarizer

include 'func_nitems.f90'
